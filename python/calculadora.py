#solicitud de dos numeros
numero1 = int(input("ingresa el numero 1: "))
numero2 = int(input("ingresa el numero 2: "))

#operaciones
suma = numero1 + numero2
print(f"la suma de los dos numero es: {suma}")

resta = numero1 - numero2
print(f"la resta de los dos numeros es: {resta}")

multiplicacion = numero1 * numero2
print(f"la multiplicacion de los dos numeros es: {multiplicacion}")

division = numero1 / numero2
print(f"la division de ios dos numeros es: {division}")